# @lc code=start
class Solution:
    def rotate(self, nums: List[int], k: int) -> None:
        """
        Do not return anything, modify nums in-place instead.
        """
        i = 0
        while i < k:
            nums.insert(0,nums[len(nums)-1])
            nums.pop(len(nums)-1)
            i+=1
# @lc code=end

